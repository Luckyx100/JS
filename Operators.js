console.log( 5 % 2 );  // "%" gives you remainder 

console.log( 5 / 2 );  // "/" actually divides the values and decimal value is included in division.

console.log( 5 == 2 );   // "==" is to compare values not to assign values.
// 5 != 2, Therefore It is False

console.log( 4 <= 6 );  // true

console.log( 7 == '7' ); // integer and String are compared for ==
console.log( 7 === '7' ); // integer and String are not compared for ===


// Logical Operator
console.log( false && true );  // And Operator
